#include <stdio.h>

int main(int argc, char const *argv[]) {
    // Declare the variable without assigning it a value
    int age;

    // Declare the variable and assign it an initial value of 5
    int level = 5;

    printf("age: %d\n", age); // This will be some random gibberish number
    printf("level: %d\n", level); // This will be 5

    return 0;
}
