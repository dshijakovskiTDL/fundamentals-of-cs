numbers = [10, 20, 30, 40, 50]

print(numbers[0])  # "10": The first (0 - indexed) element
print(numbers[1])  # "20": The second (1 - indexed) element
print(numbers[2])  # "30": The third (2 - indexed) element
print(numbers[3])  # "40": The fourth (3 - indexed) element
print(numbers[4])  # "50": The fifth (4 - indexed) element
